function hexToHslStr(hex: string | undefined | null): string {
  if (!hex || typeof hex !== "string") return "0 0% 0%";
  let c = hex.replace("#", "");
  if (c.length === 3) c = c.split("").map((ch) => ch + ch).join("");
  if (c.length === 8) c = c.slice(0, 6); // ignore alpha for hsl()
  if (c.length !== 6) return "0 0% 0%";
  const num = parseInt(c, 16);
  const r = (num >> 16) & 255;
  const g = (num >> 8) & 255;
  const b = num & 255;
  const r1 = r / 255, g1 = g / 255, b1 = b / 255;
  const max = Math.max(r1, g1, b1), min = Math.min(r1, g1, b1);
  let h = 0, s = 0, l = (max + min) / 2;
  if (max !== min) {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r1:
        h = (g1 - b1) / d + (g1 < b1 ? 6 : 0);
        break;
      case g1:
        h = (b1 - r1) / d + 2;
        break;
      default:
        h = (r1 - g1) / d + 4;
    }
    h /= 6;
  }
  const H = Math.round(h * 360);
  const S = Math.round(s * 100);
  const L = Math.round(l * 100);
  return `${H} ${S}% ${L}%`;
}

export function getSmartkitTokens(mode: Mode) {
  const colors = createDerivedColors(mode);
  const aliases = createColorAliases(colors, mode);
  const components = createColorComponentAliases(colors, aliases, mode);
  return { colors, aliases, components };
}

